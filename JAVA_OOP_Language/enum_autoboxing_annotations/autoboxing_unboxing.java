public class autoboxing_unboxing {
    public static void main(String[] args) {
        //auto type wrap 
        Integer aobj = 40; // aobj  Integer is a object that includes many functions 
        int d= aobj; // unwrapping in int
        System.out.println(d);
        System.out.println(aobj);
        //same for Double Character and all primitive data etcc note double<>Double int<>Integer
    }
}
